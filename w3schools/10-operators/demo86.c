// W3Schools, C Operators
#include <stdio.h>

int main() {
    // Sizeof Operator
    int myInt;
    float myFloat;
    double myDouble;
    char myChar;

    // long unsigned int (%lu)
    printf("%lu\n", sizeof(myInt));
    printf("%lu\n", sizeof(myFloat));
    printf("%lu\n", sizeof(myDouble));
    printf("%lu\n", sizeof(myChar));

    return 0;
}
