#include <stdio.h>

int main() {
    // Declare an array of four integers:
    int myNumbers[4];

    // Add elements to it
    myNumbers[0] = 25;
    myNumbers[1] = 50;
    myNumbers[2] = 75;
    myNumbers[3] = 100;

    printf("%d\n", myNumbers[0]);
    printf("%d\n", myNumbers[1]);
    printf("%d\n", myNumbers[2]);
    printf("%d\n", myNumbers[3]);
    return 0;
}
