// W3Schools, C Variables, Real-Life Examples
#include <stdio.h>

int main() {
    // Student data
    int studentID = 15;
    int studentAge = 23;
    float studentFee = 75.23;
    char studentGrade = 'B';

    // Print variables
    printf("Student id: %d\n", studentID);
    printf("Student age: %d\n", studentAge);
    printf("Student fee: %f\n", studentFee);
    printf("Student grade: %c\n", studentGrade);
    return 0;
}
